#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*str;
	int		i;
	int		j;

	i = 0;
	j = -1;
	while (s1[i])
	{
		while (set[++j])
			if (s1[i] == set[j])
				++i;
	}
}


