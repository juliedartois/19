#include "printf.h"

int				ft_printf(const char *s, ...)
{
	int		i;

	i = -1;
	while (s[++i])
	{
		if (s[i] == '%')
		{
			if (s[i + 1] == 'c')
				;
			else if (s[i + 1] == 'd')
				;
			else if (s[i + 1] == 'e')
				;
			else if (s[i + 1] == 'f')
				;
			else if (s[i + 1] == 'o')
				;
			else if (s[i + 1] == 's')
				;
			else if (s[i + 1] == '%')
				write(1, '%', 1);
		}
		else
			write(1, &s[i], 1);
	}
}
